package step8;

public class Shop {
	private String name;
	private Product product;
	
	public void setName(String name) {
		this.name = name;
	}
	
	public void setProduct(Product product) {
		this.product = product;
	}
	
	public String getName() {
		return name;
	}
	
	public Product getProduct() {
		return product;
	}
}
